/* El comando 'SELECT' se utiliza para consultar datos de una o más tablas
dentro de una base de datos*/


-- Selección de todas las columnas de la tabla users
SELECT * FROM users;

-- Obtiene la columna name de la tabla "users"
SELECT name FROM users;

-- Obtiene la columna first_name, last_name, email de la tabla "users"
SELECT first_name, last_name, email FROM users

